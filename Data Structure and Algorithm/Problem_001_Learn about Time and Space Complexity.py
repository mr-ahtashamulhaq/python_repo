# Time complexity tells us how the running time of an algorithm grows as the input size (n) increases.

# Space complexity measures how much extra memory your algorithm uses in addition to the input.

"""O(1) → Constant time: doesn’t depend on input size.

O(log n) → Logarithmic time: grows slowly.

O(n) → Linear time: grows directly with input size.

O(n log n) → Log-linear time: faster than quadratic but slower than linear.

O(n²) → Quadratic time: nested loops."""